package kr.palmapps.palmpay_dev_ver4.Item;

public class OrderListItem {

    String order_name;
    String order_each_price;
    String order_count;
    String order_total_price;

    public String getOrder_name() {
        return order_name;
    }

    public void setOrder_name(String order_name) {
        this.order_name = order_name;
    }

    public String getOrder_each_price() {
        return order_each_price;
    }

    public void setOrder_each_price(String order_each_price) {
        this.order_each_price = order_each_price;
    }

    public String getOrder_count() {
        return order_count;
    }

    public void setOrder_count(String order_count) {
        this.order_count = order_count;
    }

    public String getOrder_total_price() {
        return order_total_price;
    }

    public void setOrder_total_price(String order_total_price) {
        this.order_total_price = order_total_price;
    }
}
